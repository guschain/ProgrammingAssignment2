#makeCacheMatrix creates an object that stores a matrix and its inverse. 
#cacheSolve computes and retrieves the inverse from cache if already calculated, avoiding costly recomputation. 
#Both functions allow for efficient caching of matrix inversion.

makeCacheMatrix <- function(x = matrix()) {
  inv <- NULL
  set <- function(y) {
    x <<- y
    inv <<- NULL
  }
  get <- function() x
  setInverse <- function(inverse) inv <<- inverse
  getInverse <- function() inv
  list(set = set, get = get, setInverse = setInverse, getInverse = getInverse)
}


